package com.example.a13797.gznews.entity;

public class UserNews {
    private String usernewsId;
    private String usernewsTitle;
    private String usernewsContent;
    private String usernewsImg;
    private String usersId;

    public String getUsernewsId() {
        return usernewsId;
    }

    public void setUsernewsId(String usernewsId) {
        this.usernewsId = usernewsId;
    }

    public String getUsernewsTitle() {
        return usernewsTitle;
    }

    public void setUsernewsTitle(String usernewsTitle) {
        this.usernewsTitle = usernewsTitle;
    }

    public String getUsernewsContent() {
        return usernewsContent;
    }

    public void setUsernewsContent(String usernewsContent) {
        this.usernewsContent = usernewsContent;
    }

    public String getUsernewsImg() {
        return usernewsImg;
    }

    public void setUsernewsImg(String usernewsImg) {
        this.usernewsImg = usernewsImg;
    }

    public String getUsersId() {
        return usersId;
    }

    public void setUsersId(String usersId) {
        this.usersId = usersId;
    }
}
